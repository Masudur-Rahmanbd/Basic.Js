// digit number , swicth

var digit = prompt("enter a digit :");
switch(digit){
    case "0":
        document.write("zero");
        break;
        case "1":
            document.write("one");
            break;
            case "2":
                document.write("two");
                break;
                case "3":
                    document.write("three");
                    default:
                        document.write("not a digit");
        
}